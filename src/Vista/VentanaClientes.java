    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int cedula = Integer.parseInt(txtCedula.getText());
        String nombre = txtNombreCliente.getText();
        String apellido = txtApellidoCliente.getText();
        Date fechaIngre = jDateFcehaNaciCliente.getDate();
        java.sql.Date fecha_nacimiento = new java.sql.Date(fechaIngre.getYear(), fechaIngre.getMonth(), fechaIngre.getDay());
        String correo = txtCorreo.getText();
        String User = txtUsuario.getText();

        if (cedula > 0 && !nombre.isEmpty() && !apellido.isEmpty() && !correo.isEmpty()) {
            if (Controller.modificarCliente(cedula, nombre, apellido, fecha_nacimiento, correo, User)) {
                JOptionPane.showMessageDialog(null, "Editado con 辿xito");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo editar el registro de c辿dula: " + cedula);

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

