    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:

        int cedula = Integer.parseInt(JOptionPane.showInputDialog("Si desea eliminar su perfil ingrese su cedula: "));
        if (cedula != 0) {
            try {
                if (Controller.solicitudEliminarCliente(cedula)) {
                    JOptionPane.showMessageDialog(null, "Eliminado con 辿xito");
                    limpiar();

                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar el cliente con cedula: " + cedula);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Ingrese una cedula valida");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese la cedula del cliente que se quiere eliminar");

        }
    }//GEN-LAST:event_jButton9ActionPerformed

