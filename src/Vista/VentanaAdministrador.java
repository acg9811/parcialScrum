    private void btnGuardarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEmpleadoActionPerformed
        // TODO add your handling code here:
        if (txtCedulaEmpleado.getText().isEmpty() || txtNombreEmpleado.getText().isEmpty()
                || txtApellidoEmpleado.getText().isEmpty() || cbCargoBanco.getSelectedIndex() == 0
                || txtUsuarioEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios");
        } else {
            int cedula = Integer.parseInt(txtCedulaEmpleado.getText());
            String nombre_empleado = txtNombreEmpleado.getText();
            String apellido_empleado = txtApellidoEmpleado.getText();

            Date fechaNaci = jDateFechaNacimientoEmpleado.getDate();
            java.sql.Date fNaci = new java.sql.Date(fechaNaci.getYear(), fechaNaci.getMonth(), fechaNaci.getDay());
            Date fechaIngre = jDateFechaIngresoEmpleado.getDate();
            java.sql.Date fIngre = new java.sql.Date(fechaIngre.getYear(), fechaIngre.getMonth(), fechaIngre.getDay());

            int cargo = cbCargoBanco.getSelectedIndex();
            int sucursal = cbSucursalEmpleado.getSelectedIndex();
            String usuario = txtUsuarioEmpleado.getText();
            String password = txtPassword.getText();
            if (ControllerEmpleado.buscarEmpleado(cedula).getCedula() != 0) {
                JOptionPane.showMessageDialog(this, "El empleado ya existe");
            } else {
                if (ControllerUsuario.buscarUsuario(usuario).getId() != 0) {
                    JOptionPane.showMessageDialog(this, "El usuario ya existe");
                } else {
                    if (ControllerUsuario.GuardarUsuario(usuario, password)
                            && ControllerEmpleado.guardarEmpleado(cedula, nombre_empleado, apellido_empleado, fNaci, fIngre, cargo, sucursal, usuario)) {
                        JOptionPane.showMessageDialog(this, "Guardado exitosamente");
                        limpiar();
                        listarEmpleados();
                        cargarGerente();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al guardar");
                    }

                }
            }
        }
    }//GEN-LAST:event_btnGuardarEmpleadoActionPerformed

