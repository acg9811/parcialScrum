    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        // TODO add your handling code here:
        if (txtCedulaRegistro.getText().isEmpty() || txtNombreRegistro.getText().isEmpty() || txtApellidosRegistro.getText().isEmpty()
                || txtCorreoRegistro.getText().isEmpty() || txtUsuarioRegistro.getText().isEmpty()
                || txtContraseñaRegistro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese todos los datos");
        } else {
            String nombre = txtNombreRegistro.getText();
            int cedula = Integer.parseInt(txtCedulaRegistro.getText());
            String apellido = txtApellidosRegistro.getText();
            String correo = txtCorreoRegistro.getText();
            String Password = txtContraseñaRegistro.getText();
            String usuario = txtUsuarioRegistro.getText();
            Date fechaNaci = jDateFechaNaciRegistro.getDate();
            java.sql.Date fecha_nacimiento = new java.sql.Date(fechaNaci.getYear(), fechaNaci.getMonth(), fechaNaci.getDay());

            if (!validarCorreo(correo)) {
                JOptionPane.showMessageDialog(null, "Ingrese un correo correcto");
                return;
            }

            if (txtContraseñaRegistro.getText().equals(txtConfirmarContraseña.getText())) {

                if (controladorCliente.buscarCliente(cedula).getCedula()!=0) {
                    JOptionPane.showMessageDialog(this, "El Cliente ya existe");
                } else {
                    if (controladorUsu.buscarUsuario(usuario).getId()!=0) {
                        JOptionPane.showMessageDialog(this, "El usuario ya existe");
                        limpiarUsuario();

                    } else {
                        if (controladorUsu.GuardarUsuario(usuario, Password)
                                && controladorCliente.SolicitudGuardarCliente(cedula, nombre, apellido, fecha_nacimiento, correo, usuario)) {
                            JOptionPane.showMessageDialog(this, "Guardado exitosamente");
                            new Login().setVisible(true);
                            this.dispose();
                        } else {
                            JOptionPane.showMessageDialog(this, "Error al guardar");
                        }

                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "las contraseñas no coninciden");
                limpiarContraseña();

            }
        }


    }//GEN-LAST:event_btnRegistroActionPerformed

